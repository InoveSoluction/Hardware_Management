 void verificarUsuario(String usuario,String senha)
    {
        try {
             Class.forName("com.mysql.cj.jdbc.Driver"); 
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/exemplo","root","");
            String sql ="select * from cadastro where Usuario=? and Senha=? ";
            PreparedStatement pst =con.prepareStatement(sql);
            
            pst.setString(1, usuario);
            pst.setString(2, senha);
            ResultSet rs=pst.executeQuery();
            if(rs.next())
            {
                ProdutosVenda ler = new ProdutosVenda();
                ler.show();
                 this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Conta nao Existe!");
            }
            
            
        } catch (Exception e) {
        }
    }
